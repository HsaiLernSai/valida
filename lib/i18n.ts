function getNestedValue(source: TranslationTree, key: string): string | undefined {
  const value = key.split(".").reduce<string | TranslationTree | undefined>((current, part) => {
    if (typeof current !== "object" || current === null) return undefined;
    return current[part];
  }, source);
  return typeof value === "string" ? value : undefined;
}

export function normalizeLanguage(value: string | null | undefined): LanguageCode {
  return supportedLanguages.some((language) => language.code === value) ? value as LanguageCode : defaultLanguage;
}

export function translate(language: LanguageCode, key: string, values?: Record<string, string | number>): string {
  const fallback = getNestedValue(translations.en, key) ?? key;
  const template = getNestedValue(translations[language], key) ?? fallback;
  if (!values) return template;
  return Object.entries(values).reduce((text, [name, value]) => text.replaceAll(`{${name}}`, String(value)), template);
}
